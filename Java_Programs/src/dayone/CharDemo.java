package dayone;

public class CharDemo {
public static void main(String[] args) {
		
		char a='A';
		System.out.println("Simple char asignment : "+a);
		
		char b = 75;
		System.out.println("ASCII code ( Int to Char ) : "+b);
		
		char c = '\u213F';
		System.out.println("UNICODE REPRESENTATION : "+c);
		
		char d = '\u2017';
		System.out.println("UNICODE REPRESENTATION : "+d);
		
		int e = 'M';
		System.out.println("ASCII code ( Char to Int ) : "+e);


	}
}
