package daytwo;

public class ForLoopExample {
	public static void main(String[] args) 
	{	
		System.out.println("Ascending order........\n");
		for(int i = 1; i<=10; i++)   
		{	
			System.out.print("Value of i:");
			System.out.println(i);
		}
		System.out.println();
		System.out.println("Descending order........\n");
		for(int i = 10; i>0; i--)   
		{	
			System.out.print("Value of i:");
			System.out.println(i);
		}
	}
}
